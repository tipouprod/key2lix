  let currentLang = localStorage.getItem('key2lix_lang') || 'en';

  function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('key2lix_lang', lang);

    document.documentElement.lang = lang === 'ar' ? 'ar' : 'en';
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (texts[lang] && texts[lang][key]) el.textContent = texts[lang][key];
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (texts[lang] && texts[lang][key]) el.placeholder = texts[lang][key];
    });

    document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-aria');
      if (texts[lang] && texts[lang][key]) el.setAttribute('aria-label', texts[lang][key]);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-title');
      if (texts[lang] && texts[lang][key]) el.setAttribute('title', texts[lang][key]);
    });

    // Update page title for known pages (e.g. account page)
    var path = (window.location && window.location.pathname) || '';
    if (path.indexOf('/client-account') !== -1 && texts[lang] && texts[lang].myAccount) {
      document.title = texts[lang].myAccount + ' - Key2lix';
    }
    if (path.indexOf('/client-register') !== -1 && texts[lang] && texts[lang].clientRegister) {
      document.title = texts[lang].clientRegister + ' - Key2lix';
    }
    if (path.indexOf('/client-login') !== -1 && texts[lang] && texts[lang].clientLogin) {
      document.title = texts[lang].clientLogin + ' - Key2lix';
    }
    if (path.indexOf('/login') !== -1 && path.indexOf('/client-login') === -1 && texts[lang] && texts[lang].adminLoginHeading) {
      document.title = texts[lang].adminLoginHeading + ' - Key2lix';
    }
    if (path.indexOf('/vendor-login') !== -1 && texts[lang] && texts[lang].vendorLoginHeading) {
      document.title = texts[lang].vendorLoginHeading + ' - Key2lix';
    }
    if (path.indexOf('/vendor-register') !== -1 && texts[lang] && texts[lang].vendorRegisterHeading) {
      document.title = texts[lang].vendorRegisterHeading + ' - Key2lix';
    }
    if (path.indexOf('/admin') !== -1 && texts[lang] && texts[lang].adminDashboard) {
      document.title = texts[lang].adminDashboard + ' - Key2lix';
    }
    if (path === '/vendor' && texts[lang] && texts[lang].vendorDashboard) {
      document.title = texts[lang].vendorDashboard + ' - Key2lix';
    }

    /* الافتراضي: عند أول زيارة (بدون عملة محفوظة) نعيّن DZD. الموقع الافتراضي: EN + DZD */
    try {
      if (typeof window.Key2lixCurrency !== 'undefined' && !localStorage.getItem('key2lix_currency')) {
        window.Key2lixCurrency.apply('DZD');
      }
    } catch (e) {}
    var footerLangLabel = document.getElementById('footer-lang-currency-label');
    var cur = (typeof window.Key2lixCurrency !== 'undefined' && window.Key2lixCurrency.current()) ? window.Key2lixCurrency.current() : 'DZD';
    if (footerLangLabel) footerLangLabel.textContent = (lang === 'ar' ? 'AR' : 'EN') + ' / ' + cur;
    var langCurrencyLabel = document.getElementById('lang-currency-label');
    if (langCurrencyLabel) langCurrencyLabel.textContent = (lang === 'ar' ? 'AR' : 'EN') + ' / ' + cur;
    try {
      document.dispatchEvent(new CustomEvent('key2lix:languageChange', { detail: { lang: lang } }));
    } catch (e) {}
  }

  window.Key2lixLang = {
    get: function (key) { return (texts[currentLang] && texts[currentLang][key]) || key; },
    current: function () { return currentLang; },
    apply: applyLanguage,
    texts: texts
  };

  document.addEventListener('DOMContentLoaded', function () {
    applyLanguage(currentLang);
    /* ربط زر اللغة يتم في common.js بعد تحميل الـ navbar (bindLangDropdown) */
  });
